# Append
list = [2, 1, 3]
list.append(4)
print(list)

# Sort
list = [2, 1, 3]
print(list.append(4))
print(list.sort())
print(list)


# sort(reverse = True)
list = [2, 1, 3]
print(list.append(4))
print(list.sort(reverse=True))
print(list)

# reverse
list = ["b", "c", "f", "g", "a"]
list.reverse()
print(list)

# insert
list = [2, 1, 4, 6, 7]
list.insert(1,9)
print(list)


# remove
list = [1, 5, 7, 8, 4]
list.remove(5)
print(list)


# pop
list = [1, 5, 7, 8, 4]
list.pop(3)
print(list)